package load;

public class Student {
	private String stt;
	private String masv;
	private String holot;
	private String ten;
	private String ngaysinh;
	private String malop;
	private String tenlop;
	private String dthoai;
	private String email;
	private String quequan;
	private String ghichu;
	
	public Student() {
	}

	public Student(String stt, String masv, String holot, String ten, String ngaysinh, String malop, String tenlop,
			String dthoai, String email, String quequan, String ghichu) {
		super();
		this.stt = stt;
		this.masv = masv;
		this.holot = holot;
		this.ten = ten;
		this.ngaysinh = ngaysinh;
		this.malop = malop;
		this.tenlop = tenlop;
		this.dthoai = dthoai;
		this.email = email;
		this.quequan = quequan;
		this.ghichu = ghichu;
	}

	public String getStt() {
		return stt;
	}

	public void setStt(String stt) {
		this.stt = stt;
	}

	public String getMasv() {
		return masv;
	}

	public void setMasv(String masv) {
		this.masv = masv;
	}

	public String getHolot() {
		return holot;
	}

	public void setHolot(String holot) {
		this.holot = holot;
	}

	public String getTen() {
		return ten;
	}

	public void setTen(String ten) {
		this.ten = ten;
	}

	public String getNgaysinh() {
		return ngaysinh;
	}

	public void setNgaysinh(String ngaysinh) {
		this.ngaysinh = ngaysinh;
	}

	public String getMalop() {
		return malop;
	}

	public void setMalop(String malop) {
		this.malop = malop;
	}

	public String getTenlop() {
		return tenlop;
	}

	public void setTenlop(String tenlop) {
		this.tenlop = tenlop;
	}

	public String getDthoai() {
		return dthoai;
	}

	public void setDthoai(String dthoai) {
		this.dthoai = dthoai;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getQuequan() {
		return quequan;
	}

	public void setQuequan(String quequan) {
		this.quequan = quequan;
	}

	public String getGhichu() {
		return ghichu;
	}

	public void setGhichu(String ghichu) {
		this.ghichu = ghichu;
	}

}
